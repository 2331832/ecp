  `ID` varchar(30) NOT NULL COMMENT 'id',
  `SN` varchar(50) NOT NULL COMMENT '玩家编号',
  `IS_M` smallint(6) NOT NULL DEFAULT '0' COMMENT '是否管理用户',
  `NICK_NAME` varchar(50) NOT NULL COMMENT '玩家昵称',
  `REFEREE_SN` varchar(50) DEFAULT NULL COMMENT '推荐玩家编号',
  `CREATER_ID` varchar(30) DEFAULT NULL COMMENT '创建人id',
  `LEVEL` smallint(6) NOT NULL DEFAULT '0' COMMENT '身份等级',
  `QQ` varchar(0) DEFAULT NULL COMMENT 'qq',
  `CREDIT_INDEX` smallint(6) NOT NULL DEFAULT '0' COMMENT '信用指数',
  `STATUS` smallint(6) NOT NULL DEFAULT '0' COMMENT '状态',
  `ACCOUNT_TYPE` smallint(6) NOT NULL DEFAULT '0' COMMENT '帐号类型',
  `REPORT_CENTER` smallint(6) NOT NULL DEFAULT '0' COMMENT '是否保单中心',
  `PWD1` varchar(50) NOT NULL COMMENT '一级密码',
  `PWD2` varchar(50) NOT NULL COMMENT '二级密码',
  `LAST_LOGIN_TIME` datetime DEFAULT NULL COMMENT '上次登录时间',
  `LAST_LOGIN_IP` varchar(50) DEFAULT NULL COMMENT '上次登录ip',
  `LOGIN_TIME` datetime DEFAULT NULL COMMENT '登录时间',
  `LOGIN_IP` varchar(50) DEFAULT NULL COMMENT '本次登录ip',
  `LOGIN_COUNT` int(11) DEFAULT '0' COMMENT '登录次数',
  `IDENTITY_CARD` varchar(30) DEFAULT NULL COMMENT '身份证号',
  `SAFE_EMAIL` varchar(50) DEFAULT NULL COMMENT '安全邮箱',
  `GOLD_COIN` decimal(10,4) DEFAULT '0.0000' COMMENT '金币',
  `GOLD_SEEDS` decimal(10,4) DEFAULT '0.0000' COMMENT '金种子',
  `FHCJ` smallint(6) DEFAULT '0' COMMENT '分红出局',
  `BONUS_0` decimal(10,4) DEFAULT '0.0000' COMMENT '累计市场分红',
  `BONUS_1` decimal(10,4) DEFAULT '0.0000' COMMENT '累计领导奖',
  `BONUS_2` decimal(10,4) DEFAULT '0.0000' COMMENT '累计报单奖',
  `BONUS_3` decimal(10,4) DEFAULT NULL COMMENT '累计推荐奖',
  `REAL_NAME` varchar(20) DEFAULT NULL COMMENT '真实姓名',
  `MOBILE` varchar(20) DEFAULT NULL COMMENT '手机',
  `ACCOUNT_COUNT_0` smallint(6) NOT NULL DEFAULT '1' COMMENT '游戏帐号总数',
  `ACCOUNT_COUNT_1` smallint(6) NOT NULL DEFAULT '0' COMMENT '直属游戏帐号',
  `ACCOUNT_COUNT_2` smallint(6) NOT NULL DEFAULT '1' COMMENT '游戏帐号有效数',
  `ACCOUNT_COUNT_3` smallint(6) NOT NULL DEFAULT '0' COMMENT '新增游戏帐号数',
  `MODIFY_PWD` smallint(6) DEFAULT '0' COMMENT '是否修改默认密码',
  PRIMARY KEY (`ID`),
  UNIQUE KEY `SN` (`SN`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户';

